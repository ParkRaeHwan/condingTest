package sparta.level1;

import java.util.Scanner;

// Programmers 두 수의 합 구하기
/*
    문제 : 입력된 두수의 합을 구하는 로직 작성
 */
public class No6 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int i = sc.nextInt();
        int j = sc.nextInt();
        System.out.println(i + j);
        sc.close();
    }
}
